results in a percentage of that transaction value being sent to the content creator/author, incentivizing their 
participation on the platform. Thus, both content creators and consumers are incentivized to be on the platform. 
The supply would always be limited, as new NFTs are only generated at the time of an e-book sale from the 
author/publisher, hence ensuring digital scarcity. We believe a free-market economics model would drive down 
the prices of resales and rentals, making it affordable for the Third World (or people in Levels 1 to 3) and 
other emerging markets, nudging people away from piracy.<br><br>
