new set of behaviors that opens up for the owner, which doesn’t exist in any of the current content distribution 
platforms.An owner can sell or rent the book as she pleases, for the price that she chooses, to anyone in the globe.<br><br>

The books will have the history of its previous owners as it jumps from hand to hand, and it becomes verifiable 
